package com.beyondTech.lSecondMax.firstsecondmin;

public class FindMaxSecondPosition {

	public static void main(String[] args) {
		int[] input = { 12, 10, 15, 16, 8, 6, 2, 4, 9, 13 };
		for (int i = input.length - 2; i <= input.length - 2; i++) {

			for (int j = input.length - 9; j <= input.length - 9; j++) {
       
				System.out.println("Min Second Position : " + input[j] + "   Max Second Position: " + input[i]);
			}
		}

	}

}
